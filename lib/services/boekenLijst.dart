      boekenLijst[index].bezit = true;
    }
  }

  void ontbrekendeBoeken() {
    for (var i = 0; i < boekenLijst.length; i++) {
      for (var x = 0; x < ontbrekende.length; x++) {
        if (ontbrekende[x].titel == boekenLijst[i].titel) {
          boekenLijst[i].foto = ontbrekende[x].foto;
          boekenLijst[i].inhoud = ontbrekende[x].inhoud;
          boekenLijst[i].jaar = ontbrekende[x].jaar;
        }
      }
    }
  }

